List<int> findTargetOld(List<int> arr, int target) {
  final stopwatch = Stopwatch()..start();

  int len = arr.length;

  for (int i = 0; i < len - 1; i++) {
    for (int j = i + 1; j < len; j++) {
      if (arr[i] + arr[j] == target) {
        stopwatch.stop();
        print('Time elapsed: ${stopwatch.elapsedMicroseconds} microseconds');
        return [i, j];
      }
    }
  }

  stopwatch.stop();
  print('Time elapsed: ${stopwatch.elapsedMicroseconds} microseconds');
  return [];
}

List<int> findTargetNew(List<int> arr, int target) {
  final stopwatch = Stopwatch()..start();
  Map<int, int> map = {};

  for (int i = 0; i < arr.length; i++) {
    if (map.containsKey(arr[i])) {
      stopwatch.stop();
      print('Time elapsed: ${stopwatch.elapsedMicroseconds} microseconds');
      return [map[arr[i]]!, i];
    }

    map[target - arr[i]] = i;
  }
  stopwatch.stop();
  print('Time elapsed: ${stopwatch.elapsedMicroseconds} microseconds');
  return [];
}
